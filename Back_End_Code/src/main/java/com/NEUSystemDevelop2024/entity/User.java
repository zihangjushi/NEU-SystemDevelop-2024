package com.NEUSystemDevelop2024.entity;

import java.sql.Timestamp;

public class User {
    private int userId;
    private int companyId;
    private int departmentId;
    private String userName;
    private String realName;
    private int gender;
    private String password;
    private String role;
    private int enabled;
    private String career;
    private String phoneNumber;
    private String email;
    private String description;
    private Timestamp createTime;

    public User() {
    }

    public User(int companyId, String userName, String realName, int gender, String password, String role, int enabled, String career, String phoneNumber, String email, Timestamp createTime, String description, int departmentId) {
        this.companyId = companyId;
        this.userName = userName;
        this.realName = realName;
        this.gender = gender;
        this.password = password;
        this.role = role;
        this.enabled = enabled;
        this.career = career;
        this.phoneNumber = phoneNumber;
        this.email = email;
        this.createTime = createTime;
        this.description = description;
        this.departmentId = departmentId;
    }

    public int getUserId() {
        return userId;
    }

    public void setUserId(int userId) {
        this.userId = userId;
    }

    public int getCompanyId() {
        return companyId;
    }

    public void setCompanyId(int companyId) {
        this.companyId = companyId;
    }

    public int getDepartmentId() {
        return departmentId;
    }

    public void setDepartmentId(int departmentId) {
        this.departmentId = departmentId;
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getRealName() {
        return realName;
    }

    public void setRealName(String realName) {
        this.realName = realName;
    }

    public int getGender() {
        return gender;
    }

    public void setGender(int gender) {
        this.gender = gender;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getRole() {
        return role;
    }

    public void setRole(String role) {
        this.role = role;
    }

    public int getEnabled() {
        return enabled;
    }

    public void setEnabled(int enabled) {
        this.enabled = enabled;
    }

    public String getCareer() {
        return career;
    }

    public void setCareer(String career) {
        this.career = career;
    }

    public String getPhoneNumber() {
        return phoneNumber;
    }

    public void setPhoneNumber(String phoneNumber) {
        this.phoneNumber = phoneNumber;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public Timestamp getCreateTime() {
        return createTime;
    }

    public void setCreateTime(Timestamp createTime) {
        this.createTime = createTime;
    }
}
